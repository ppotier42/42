#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"

void	function1(HumanA & bob)
{
	bob.attack();
}

int	main(void)
{
	{
	Weapon club = Weapon("crude spiked club");
	HumanA bob("Bob", club);
	bob.attack();
	club.setType("some other type of club");
	bob.attack();
	function1(bob);
	}
	{
	Weapon club = Weapon("crude spiked club");
	HumanB jim("Jim");
	jim.attack();
	jim.setWeapon(club);
	club.setType("some other type of club");
	jim.attack();
	}
	return 0;
}